package com.innodealing.model;

public class CompRRS {
    Long comUniCode;
    Long compId;
    String rating;
    Long pdOrder;
    Long modelId;
    String modelName;
    Double score;
    Integer year;
    
    /**
     * @return the compId
     */
    public Long getCompId() {
        return compId;
    }
    /**
     * @param compId the compId to set
     */
    public void setCompId(Long compId) {
        this.compId = compId;
    }
    /**
     * @return the modelId
     */
    public Long getModelId() {
        return modelId;
    }
    /**
     * @param modelId the modelId to set
     */
    public void setModelId(Long modelId) {
        this.modelId = modelId;
    }
    /**
     * @return the modelName
     */
    public String getModelName() {
        return modelName;
    }
    /**
     * @param modelName the modelName to set
     */
    public void setModelName(String modelName) {
        this.modelName = modelName;
    }
    /**
     * @return the score
     */
    public Double getScore() {
        return score;
    }
    /**
     * @param score the score to set
     */
    public void setScore(Double score) {
        this.score = score;
    }
    /**
     * @return the comUniCode
     */
    public Long getComUniCode() {
        return comUniCode;
    }
    /**
     * @param comUniCode the comUniCode to set
     */
    public void setComUniCode(Long comUniCode) {
        this.comUniCode = comUniCode;
    }
    /**
     * @return the rating
     */
    public String getRating() {
        return rating;
    }
    /**
     * @param rating the rating to set
     */
    public void setRating(String rating) {
        this.rating = rating;
    }
    /**
     * @return the pdOrder
     */
    public Long getPdOrder() {
        return pdOrder;
    }
    /**
     * @param pdOrder the pdOrder to set
     */
    public void setPdOrder(Long pdOrder) {
        this.pdOrder = pdOrder;
    }
    /**
     * @return the year
     */
    public Integer getYear() {
        return year;
    }
    /**
     * @param year the year to set
     */
    public void setYear(Integer year) {
        this.year = year;
    }
    
    
}
